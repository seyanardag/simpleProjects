


function changeColorFunc(value) {
  switch (value) {
    case "blue":
        document.body.style.backgroundColor="#93CAED"
      break;
    case "yellow":
        document.body.style.backgroundColor="#F3E0BE"
      break;
    case "pink":
        document.body.style.backgroundColor="#F7CAC9"
      break;

    default:
      break;
  }
}
